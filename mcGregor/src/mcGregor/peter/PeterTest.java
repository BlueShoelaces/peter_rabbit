package mcGregor.peter;

import junit.framework.TestCase;

public class PeterTest extends TestCase {
	
	public void testWhenPeterAttacksHeSwingsHisSword() throws Exception {
		Peter peter = new Peter();
		
		assertFalse(peter.hasAttacked());
		
		peter.attack();
		
		assertTrue(peter.hasAttacked());
	}

}
